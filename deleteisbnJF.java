    private void delete(String isbn)
    {
        objeto = new deleteISBN(isbn);
        
        if(objeto.existfile())
        {
            int ok = JOptionPane.showConfirmDialog(null, String.format("Dese eliminar el isbn: %s",isbn),"Java Confirm",2);
            
            if (ok==0)
            {
                objeto.deleteFile();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"No existe el ISBN","Java",0);
            cleartxt();
        }
    }
    
    

